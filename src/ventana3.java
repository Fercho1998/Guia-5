    private void btnAreaDiagonalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAreaDiagonalActionPerformed
       
        if(!txtLadoCuadrado.getText().isEmpty()){
        double numero = Double.parseDouble(this.txtLadoCuadrado.getText());
        double Area= ((numero)*(numero));
        double Diagonal= (Math.sqrt(numero*numero*2));
        txtResultado.setText("Area:"+Area+ "\nDiagonal:"+Diagonal);
        txtLadoCuadrado.setText("");
     }
        else{
            JOptionPane.showMessageDialog(null, "error");
            txtLadoCuadrado.requestFocus();
        }
    }//GEN-LAST:event_btnAreaDiagonalActionPerformed

