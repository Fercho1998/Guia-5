    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
       if(!txtIngresarDiez.getText().isEmpty()){
           double dato=Double.parseDouble(txtIngresarDiez.getText());
           vector[pos]=dato;
           txtIngresarDiez.setText("");         
           pos=pos+1;
           if(pos==10){
               txtIngresarDiez.setEnabled(false);              
           }   
           else{
             txtIngresarDiez.requestFocus();  
           }
       }         
       else{
           JOptionPane.showMessageDialog(null, "campo vacio");
           txtIngresarDiez.requestFocus();
       }
        
    }//GEN-LAST:event_btnIngresarActionPerformed

